#this is just a combinatorics problem, since you move to the right 20 times and down 20 times. You need to pick 20/40 turns to go right, so 40 choose 20.

def factorial(num_to_factorial):
  factorial_answer = 1
  for i in range (1, num_to_factorial + 1):
    factorial_answer = factorial_answer * i
  return(factorial_answer)

def n_choose_r(n,r):
  return(factorial(n) / (factorial(r) * factorial(n - r)))

print("The number of different paths is", round(n_choose_r(40,20)))

