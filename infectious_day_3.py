def digit_sum(num_to_sum):
  counter = len(str(num_to_sum))
  sum_of_digits = 0
  while counter+1 > 0:
    smallest_digit = num_to_sum % 10
    sum_of_digits = sum_of_digits + smallest_digit
    num_to_sum = num_to_sum // 10
    counter = counter - 1
  return(sum_of_digits)

