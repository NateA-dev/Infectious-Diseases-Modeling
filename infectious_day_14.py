import random
import numpy as np
import matplotlib.pyplot as plt

population_size = 100
population_counts = [population_size - 1, 1, 0]

def next_step(current_counts, R0, D, N):
  S = current_counts[0]
  I = current_counts[1]
  R = current_counts[2]

  prob_S_to_I = (R0 / D) * (I / N) if I > 0 else 0
  next_infected = np.random.binomial(S, prob_S_to_I)
  next_infected = min(next_infected, S)

  prob_I_to_R = 1 / D
  next_recovered = np.random.binomial(I, prob_I_to_R)
  next_recovered = min(next_recovered, I)

  next_S = S - next_infected
  next_I = I + next_infected - next_recovered
  next_R = R + next_recovered

  return [next_S, next_I, next_R]

