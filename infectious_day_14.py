import random
import numpy as np
import matplotlib.pyplot as plt

population_list = ['I']

for i in range (99):
  population_list.append('S')

def next_step(population_list, R0, D, contacts_per_day):
  population_size = len(population_list)
  new_population_list = list(population_list)
  prob_S_to_I_per_contact = R0 / (D * contacts_per_day)


#step 1: infected person might infect more people
  for i in range(population_size):
    if population_list[i] == 'I':
      for j in range(contacts_per_day):
        contact_person_index = random.randint(0, population_size - 1) #contact is a random person in the population
        if population_list[contact_person_index] == 'S':
          if random.random() < prob_S_to_I_per_contact:
            new_population_list[contact_person_index] = 'I'

#step 2: infected person might recover
  for i in range(population_size):
    if population_list[i] == 'I':
      if random.random() < (1/D): #picks a random number 0-1, if that is less than prob_I_to_R then the infected person becomes recovered
        new_population_list[i] = 'R'

  return new_population_list




S_list=[]
